public class Keyboard {
    private String merkModel;
    private long harga;

    public void setMerkModel(String merkModel) {
        this.merkModel = merkModel;
    }

    public void setHarga(long harga) {
        this.harga = harga;
    }

    public String getMerkModel() {
        return merkModel;
    }

    public long getHarga() {
        return harga;
    }
}
